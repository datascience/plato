package eu.scape_project.planning.services.pa;

import java.io.Serializable;

/**
 * Definition of preservation service registry
 *  
 * @author Michael Kraxner
 *
 */
public class PreservationActionRegistryDefinition implements Serializable {
    
    private static final long serialVersionUID = -2328538539051184372L;
    
    
    private String shortname;
    private String url;
    private String logo;
    private String type;
    private boolean active;
    
    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getShortname() {
        return shortname;
    }

    public void setShortname(String shortname) {
        this.shortname = shortname;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public boolean isActive() {
        return active;
    }

    public void setActive(boolean active) {
        this.active = active;
    }

}
